package ch11.logon;

import java.util.Date;

public class CoupleDataBean {
	private String coupleName;
	private Date coupleDate;
	private String id1;
	private String id2;
	private int point;
	private String coupleImage;
	public String getCoupleName() {
		return coupleName;
	}
	public void setCoupleName(String coupleName) {
		this.coupleName = coupleName;
	}
	public Date getCoupleDate() {
		return coupleDate;
	}
	public void setCoupleDate(Date coupleDate) {
		this.coupleDate = coupleDate;
	}
	public String getId1() {
		return id1;
	}
	public void setId1(String id1) {
		this.id1 = id1;
	}
	public String getId2() {
		return id2;
	}
	public void setId2(String id2) {
		this.id2 = id2;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public String getCoupleImage() {
		return coupleImage;
	}
	public void setCoupleImage(String coupleImage) {
		this.coupleImage = coupleImage;
	}
	
	
}
